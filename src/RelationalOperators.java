
public class RelationalOperators { //can return given i/p  only true or false

	public static void main(String[] args) {
		
		// > >= < <= == !=
		
		int a =10;
		int b= 45;
		
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(b<a);
		System.out.println(b<=a);
		System.out.println(a==b);
		System.out.println(a!=b);
		
	}

}
